
let luke1Btn = document.querySelector("#Luke1");
let filmbilde1 = document.getElementById("filmbilde1");
let today = new Date();
luke1Btn.onclick = () => {
    if(today.getDate()>= 1){
        filmbilde1.style.backgroundImage = 'url("assets/film1.jpeg")';
        filmbilde1.style.backgroundSize='95%'
    }
    else{
        filmbilde1.style.backgroundImage = 'url("assets/NeiLuke.png")';
        filmbilde1.style.backgroundSize='95%'
    }
}

var element = document.querySelector("#Luke1");
element.addEventListener("click", toggleDoor);
function toggleDoor() {
    this.classList.toggle("doorOpen");
  }


